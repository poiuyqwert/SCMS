const u8 CHKSectionVCOD::defaultOpcodes[16] = {1, 4, 5, 6, 2, 1, 5, 2, 0, 3, 7, 7, 5, 4, 6, 3};
CHKSectionVCOD::CHKSectionVCOD(CHK *chk)
: CHKSection(chk)
{
	memcpy(this->vcod.seed, CHKSectionVCOD::defaultSeed, 1024);
	memcpy(this->vcod.opcodes, CHKSectionVCOD::defaultOpcodes, 16);
}
void CHKSectionVCOD::load_data(const u8 *data, u32 size) {
	memcpy(&this->vcod, data, sizeof(CHKSectionVCOD::vcod));
}
u8* CHKSectionVCOD::save_data(u32 &size) {
	size = sizeof(CHKSectionVCOD::vcod);
	u8 *buffer = new u8[size];
	memcpy(buffer, &this->vcod, size);
	return buffer;
}
